let currentQuestionIndex = 0;
let score = 0;
let currentLevel = '';
let timer;
let timeLeft = 30; // 30 seconds per question

function startQuiz(level) {
    currentLevel = level; // Set the selected difficulty
    currentQuestionIndex = 0;
    score = 0;

    // Hide difficulty selection
    document.getElementById('difficulty-selection').style.display = 'none';

    // Show the quiz container
    document.getElementById('quiz-container').style.display = 'block';

    // Show the first question
    loadQuestion();
    startTimer();
}

function loadQuestion() {
    const questionData = quizData[currentLevel][currentQuestionIndex];
    document.getElementById('question').innerText = questionData.question;

    const optionsContainer = document.getElementById('options');
    optionsContainer.innerHTML = ''; // Clear previous options

    questionData.options.forEach(option => {
        const button = document.createElement('button');
        button.innerText = option;
        button.onclick = () => checkAnswer(option, questionData.correctAnswer);
        optionsContainer.appendChild(button);
    });

    // Update progress bar
    const progress = (currentQuestionIndex / quizData[currentLevel].length) * 100;
    document.getElementById('progress-bar').style.width = progress + '%';
}

function checkAnswer(selectedOption, correctAnswer) {
    const feedback = document.getElementById('feedback');
    if (selectedOption === correctAnswer) {
        score++;
        feedback.innerText = "Correct! Well done!";
        feedback.style.color = "green";
    } else {
        feedback.innerText = "Incorrect. The correct answer was: " + correctAnswer;
        feedback.style.color = "red";
    }
    document.getElementById('next-button').style.display = 'inline-block'; // Show the Next button
}

function nextQuestion() {
    currentQuestionIndex++;
    if (currentQuestionIndex < quizData[currentLevel].length) {
        loadQuestion();
        document.getElementById('feedback').innerText = ''; // Clear feedback
        document.getElementById('next-button').style.display = 'none'; // Hide the Next button
        resetTimer();
    } else {
        endQuiz(); // End the quiz if all questions are answered
    }
}

function startTimer() {
    timeLeft = 30; // Reset timer
    timer = setInterval(function() {
        document.getElementById('timer').innerText = `Time Left: ${timeLeft}s`;
        timeLeft--;
        if (timeLeft < 0) {
            clearInterval(timer); // Stop timer when it reaches zero
            nextQuestion(); // Move to next question when time is up
        }
    }, 1000);
}

function resetTimer() {
    clearInterval(timer); // Reset the timer
    startTimer(); // Start a new timer for the next question
}

function endQuiz() {
    document.getElementById('quiz-container').style.display = 'none'; // Hide the quiz container
    document.getElementById('score-container').style.display = 'block'; // Show the score container
    document.getElementById('score').innerText = `${score} out of ${quizData[currentLevel].length}`; // Display score
}


